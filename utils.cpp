int getCorrectNumber(int min, int max) {
	int result;
	wcin >> result;
	while (wcin.fail() || (result < min && result > max)) {
		wcin.clear();
		wcin.ignore(10000, '\n');
		wcout << L"Число должно быть от " << min << L" до " << max << ": ";
	}
	return result;
}


